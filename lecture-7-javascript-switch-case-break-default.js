// faster version of if else is switch

var roll = 3; // switch expression
switch (roll) {
    case 1: //if the value is number than we dont have to put '' we only have to put '' only for string 
        console.log('Mehedi hasan');
        break;
    case 2:
        console.log('Sakib');
        break;
    case 3:
        console.log('Maruf');
        break;
    case 4:
        console.log('Mithun');
        break;
    default:
        console.log('invalid roll');
}

//color switch
var color = 'violet';
switch (color) {
    case 'violet': //if the value is number than we dont have to put '' we only have to put '' only for string 
        console.log('Violet');
        break;
    case 'blue':
        console.log('Blue');
        break;
    case 'indigo':
        console.log('Indigo');
        break;
    case 'green':
        console.log('Green');
        break;
    default:
        console.log('white');
}

